package day02_variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("\tHello");
        System.out.println("\t\t\t\t\tHello");
        //it means a paragraph scape

        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool\n");
        System.out.println("My\nname\nis\nGozde");
        //it means new line

        System.out.println("My favourite TV show is\"True\"");

        //it means double quote, prints double quote to the console

        System.out.println("\\");
        //print single slash on console
        System.out.println("/");

        System.out.println("my fav.book is 'java'");
        System.out.println("my fav.book is \"java\"");




    }
}
