package day15_forLoop;

public class forLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World");//its gonna execute for infinite times.
        //initilizatin, condition, iterator otherwise it will execute infinite times.

        }
        System.out.println("================");
        for (int i = 1; i <= 10; i+=2) {
            System.out.println(i);//1 3 5 7 9

            System.out.println(i*i);


        }
    }
}